//como compara dois numeros

let ehMaior = 25 > 16;
//comparar se 25 é maior que 16, e vai retornar true. 
console.log(ehMaior);

ehMenor = 15 > 16;
//comparar se 15 é maior que 16, e vai retornar false.
console.log(ehMenor);

ehMaiorOuIgual = 16 >= 16;
//comparar se 16 é maior ou igual a 16, e vai retornar true.
console.log(ehMaiorOuIgual);

ehMenorOuIgual = 16 <= 16;
//comparar se 25 é menor que 16, e vai retornar true.
console.log(ehMenorOuIgual);

// exemplo de comparaçao usando variaveis

//usando variantes constantes, para atribuir os valores que quero comparar.
const x = 8;
const y = 16;

let comparacao = x > y;
console.log(comparacao);

comparacao = x < y;
console.log(comparacao);

comparacao = x <= y;
console.log(comparacao);

comparacao = x >= y;
console.log(comparacao);